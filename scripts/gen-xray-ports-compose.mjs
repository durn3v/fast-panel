#!/usr/bin/env node
/**
 * Собирает TCP-порты из inbounds в конфиге Xray и пишет docker-compose.xray-ports.gen.yml
 * (мерджится с базовым compose через COMPOSE_FILE в scripts/vpn-panel).
 *
 * Путь к JSON: XRAY_CONFIG_PATH (от корня репо) или по умолчанию config/xray/config.json.
 * Поле `api.port` не публикуем — только inbounds (как клиентские входы).
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outFile = join(root, "docker-compose.xray-ports.gen.yml");

const MAX_RANGE_SPAN = 1024;

function loadDotEnv() {
  const p = join(root, ".env");
  if (!existsSync(p)) return {};
  const env = {};
  for (const line of readFileSync(p, "utf8").split("\n")) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const i = t.indexOf("=");
    if (i <= 0) continue;
    const k = t.slice(0, i).trim();
    let v = t.slice(i + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    env[k] = v;
  }
  return env;
}

function resolveEnvToken(token) {
  const t = token.trim();
  if (t.startsWith("env:")) {
    const name = t.slice(4).trim();
    const v = process.env[name];
    if (v == null || v === "")
      throw new Error(`env var ${name} is not set (used in Xray port: ${t})`);
    return v;
  }
  return t;
}

/** @param {string} segment */
function expandPortSegment(segment) {
  const s = resolveEnvToken(segment);
  if (!s) return [];
  if (s.includes("-")) {
    const [aStr, bStr] = s.split("-", 2).map((x) => x.trim());
    const from = Number(aStr);
    const to = Number(bStr);
    if (!Number.isInteger(from) || !Number.isInteger(to) || from < 1 || to > 65535)
      throw new Error(`Invalid port range: ${segment}`);
    if (from > to) throw new Error(`Invalid port range (from > to): ${segment}`);
    if (to - from > MAX_RANGE_SPAN)
      throw new Error(
        `Port range too large (${segment}); max span is ${MAX_RANGE_SPAN}`
      );
    const out = [];
    for (let p = from; p <= to; p++) out.push(p);
    return out;
  }
  const p = Number(s);
  if (!Number.isInteger(p) || p < 1 || p > 65535)
    throw new Error(`Invalid port: ${segment}`);
  return [p];
}

/**
 * @param {unknown} portVal — как в Xray JSON: number | string ("443", "443,8443", "1000-1010")
 */
function portsFromPortField(portVal) {
  if (portVal === undefined || portVal === null) return [];
  if (typeof portVal === "number") return expandPortSegment(String(portVal));
  const str = String(portVal).trim();
  if (!str) return [];
  return str
    .split(",")
    .map((x) => x.trim())
    .filter(Boolean)
    .flatMap((seg) => expandPortSegment(seg));
}

/**
 * @param {Record<string, unknown>} ib — один inbound из config.inbounds
 */
function portsFromInbound(ib) {
  const proto =
    typeof ib.protocol === "string" ? ib.protocol.toLowerCase() : "";
  if (proto === "tun") return [];

  if (typeof ib.listen === "string") {
    const L = ib.listen.trim();
    if (L.startsWith("/") || L.startsWith("@")) return [];
  }

  return portsFromPortField(ib.port);
}

/**
 * @param {unknown} cfg
 * @returns {number[]}
 */
function collectInboundTcpPorts(cfg) {
  if (!cfg || typeof cfg !== "object") return [];
  const inbounds = /** @type {Record<string, unknown>} */ (cfg).inbounds;
  if (!Array.isArray(inbounds)) return [];
  const set = new Set();
  for (const raw of inbounds) {
    if (!raw || typeof raw !== "object") continue;
    for (const p of portsFromInbound(/** @type {Record<string, unknown>} */ (raw))) {
      set.add(p);
    }
  }
  return [...set].sort((a, b) => a - b);
}

const merged = { ...loadDotEnv(), ...process.env };
const configRel = merged.XRAY_CONFIG_PATH ?? "config/xray/config.json";
const configPath = join(root, configRel);

let ports = [];
if (!existsSync(configPath)) {
  console.warn(
    `[gen-xray-ports] ${configRel} not found — using fallback port 443 for compose`
  );
  ports = [443];
} else {
  try {
    const cfg = JSON.parse(readFileSync(configPath, "utf8"));
    ports = collectInboundTcpPorts(cfg);
    if (ports.length === 0) {
      console.warn(
        `[gen-xray-ports] no TCP ports in inbounds[] — using fallback 443 (see ${configRel})`
      );
      ports = [443];
    }
  } catch (e) {
    console.error(`[gen-xray-ports] failed to read ${configRel}:`, e);
    process.exitCode = 1;
    throw e;
  }
}

const portLines = ports
  .map((p) => {
    if (!Number.isInteger(p) || p < 1 || p > 65535)
      throw new Error(`Invalid port: ${p}`);
    return `      - "${p}:${p}"`;
  })
  .join("\n");

const yml = `## Generated by scripts/gen-xray-ports-compose.mjs — do not edit by hand
services:
  xray:
    ports:
${portLines}
`;

writeFileSync(outFile, yml, "utf8");
console.log("Wrote", outFile, "from", configRel, "ports:", ports.join(", "));
